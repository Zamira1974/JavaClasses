package com.class2;

public class PerimeterAndArea {

	public static void main(String[] args){
		
		
		
		int wight=5;
		int height=8;
	
		int perimeter=2*(wight+height);
		int area=wight*height;
		
		String message ="The perimeter of rectangle with width "+wight + " and height" +height+" is equal to "+perimeter + " and the area is " + area;
		
		System.out.println(message);
		
		
		
	}

}
