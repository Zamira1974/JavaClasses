package com.syntax.datatypes;

public class ArithmeticOperators {

	public static void main (String [ ] args){
		
		//Arithmetic operators
		//Assignment operators
		
		
		// +  -  /  *  %
		
		int firstNum=10;
		int secondNum=5;
		int sum=firstNum+secondNum;
		
		System.out.println(sum);
		System.out.println("***************");

		
		int firstDigit=2;
		int secondDigit=5;
		int resultofMultiplication=firstDigit*secondDigit;
		System.out.println(resultofMultiplication);
		System.out.println("***************");
		
		
		double a=10; 
		double b=3;
		double result=a/b;
		System.out.println(result);
		System.out.println("***************");
		
		
		int k=10; 
		int m=3;
		int result1=k%m;
		System.out.println(result1);
		System.out.println("***************");
		
				
		
		
		
		
		
	}
}