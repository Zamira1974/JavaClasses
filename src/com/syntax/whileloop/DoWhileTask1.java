package com.syntax.whileloop;

public class DoWhileTask1 {

	public static void main(String[] args) {

		   //   Task One
        //Write a program using a do while loop to print out all odd number from 0 15 
		
		int num=1;
		do {
		System.out.println(num);
		num+=2;
		} while(num<=15);
		}
		
	}

		
		
		
		
		
		
		
		



