package com.class13;

public class StringManipulation {

	public static void main(String[] args) {

		// substring
		// This method returns a new string that is a substring of this string.
		String str = "Java is getting interesting";

		System.out.println(str.substring(10)); // Output: tting interesting

		System.out.println(str.substring(8, 15)); // Output: getting

	}

}
